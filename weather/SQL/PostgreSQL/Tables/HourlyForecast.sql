DROP TABLE HourlyForecast;

CREATE TABLE HourlyForecast(
    HourlyForecastID int GENERATED ALWAYS AS IDENTITY,
    locationid int8 NULL,
    forecast_date timestamp NULL,
    forecast_time timestamp NULL,
    forecast_time_epoch int8 NULL,
    temp_c float NULL,
    temp_f float NULL,
    is_day bit NULL,
    forecast_condition varchar(255) NULL,
    wind_mph float NULL,
    wind_kph float NULL,
    wind_degree float NULL,
    wind_dir varchar(255) NULL,
    pressure_mb float NULL,
    pressure_in float NULL,
    precip_mm float NULL,
    precip_in float NULL,
	snow_cm float NULL, 
    humidity float NULL,
    cloud float NULL,
    feelslike_c float NULL,
    feelslike_f float NULL,
    windchill_c float NULL,
    windchill_f float NULL,
    heatindex_c float NULL,
    heatindex_f float NULL,
    dewpoint_c float NULL,
    dewpoint_f float NULL,
    will_it_rain bit NULL,
    chance_of_rain float NULL,
    will_it_snow bit NULL,
    chance_of_snow float NULL,
    vis_km float NULL,
    vis_miles float NULL,
    gust_mph float NULL,
    gust_kph float NULL,
    uv float NULL,
 PRIMARY KEY (HourlyForecastID)
 ) ;